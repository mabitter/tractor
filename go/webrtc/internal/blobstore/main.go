// RWDir is an extension of http.Dir, with support for writing files
type RWDir struct {
	Dir http.Dir
}

// RWFile is an extension of http.File, with support for writing
type RWFile interface {
	http.File
	io.Writer
}

type rwFileSystem interface {
	http.FileSystem
	OpenFile(name string, flag int, perm os.FileMode) (RWFile, error)
}

type fileHandler struct {
	root rwFileSystem
}

// Open implements FileSystem using os.Open, opening files for reading rooted
// and relative to the directory d.
func (d RWDir) Open(name string) (http.File, error) {
	return (d.Dir.Open(name))
}

// OpenFile implements FileSystem using os.OpenFile, opening files for reading rooted
// and relative to the directory d.
func (d RWDir) OpenFile(name string, flag int, perm os.FileMode) (RWFile, error) {
	if filepath.Separator != '/' && strings.ContainsRune(name, filepath.Separator) {
		return nil, errors.New("http: invalid character in file path")
	}
	dir := string(d.Dir)
	if dir == "" {
		dir = "."
	}
	fullName := filepath.Join(dir, filepath.FromSlash(path.Clean("/"+name)))
	f, err := os.OpenFile(fullName, flag, perm)
	if err != nil {
		return nil, mapDirOpenError(err, fullName)
	}
	return f, nil
}

func uploadFile(w http.ResponseWriter, r *http.Request, fs rwFileSystem, name string) {
	f, err := fs.OpenFile(name, os.O_RDWR|os.O_TRUNC, 0644) // perms not actually used in R/W mode
	if err != nil {
		msg, code := toHTTPError(err)
		http.Error(w, msg, code)
		return
	}
	defer f.Close()

	d, err := f.Stat()
	if err != nil {
		msg, code := toHTTPError(err)
		http.Error(w, msg, code)
		return
	}

	if d.IsDir() {
		logf(r, "blobstore: cannot upload to directory: %v", name)
		http.Error(w, "Error uploading", http.StatusInternalServerError)
		return
	}

	_, err = io.Copy(f, r.Body)
	if err != nil {
		logf(r, "blobstore: cannot upload to file: %v", err)
		http.Error(w, "Error uploading to file", http.StatusInternalServerError)
		return
	}

	w.WriteHeader(http.StatusOK)
}

// populates and returns a protobuf that describes the directory contents
func dirList(w http.ResponseWriter, r *http.Request, f http.File) {
	dirs, err := f.Readdir(-1)
	if err != nil {
		logf(r, "blobstore: error reading directory: %v", err)
		http.Error(w, "Error reading directory", http.StatusInternalServerError)
		return
	}
	sort.Slice(dirs, func(i, j int) bool { return dirs[i].Name() < dirs[j].Name() })

	fileInfo, err := f.Stat()
	if err != nil {
		logf(r, "blobstore: error reading fileinfo: %v", err)
		http.Error(w, "Error reading fileinfo", http.StatusInternalServerError)
		return
	}
	response := &pb.File{}
	err = marshalFileInfoToProto(fileInfo, response)
	if err != nil {
		logf(r, "blobstore: error marshaling file to proto: %v", err)
		http.Error(w, "Error marshaling file to proto", http.StatusInternalServerError)
		return
	}

	files := &response.Type.(*pb.File_Directory_).Directory.Files
	for _, d := range dirs {
		proto := &pb.File{}
		err = marshalFileInfoToProto(d, proto)
		if err != nil {
			logf(r, "blobstore: error marshaling file to proto: %v", err)
			http.Error(w, "Error marshaling file to proto", http.StatusInternalServerError)
			return
		}
		*files = append(*files, proto)
	}

	if r.Header.Get("Content-Type") == "application/protobuf" {
		w.Header().Set("Content-Type", "application/protobuf")
		out, err := proto.Marshal(response)
		if err != nil {
			logf(r, "blobstore: error marshaling response to proto: %v", err)
			http.Error(w, "Error marshaling response to proto", http.StatusInternalServerError)
			return
		}
		w.Write(out)
		return
	}
	w.Header().Set("Content-Type", "application/json")
	jsonMarshaler.Marshal(w, response)
}

// FileServer returns a handler that serves HTTP requests
// with the contents of the file system rooted at root.
//
// To use the operating system's file system implementation,
// use http.Dir:
//
//     http.Handle("/", http.FileServer(http.Dir("/tmp")))
//
// As a special case, the returned file server redirects any request
// ending in "/index.html" to the same path, without the final
// "index.html".
func FileServer(root rwFileSystem) http.Handler {
	return &fileHandler{root}
}

func (f *fileHandler) ServeHTTP(w http.ResponseWriter, r *http.Request) {
	upath := r.URL.Path
	if !strings.HasPrefix(upath, "/") {
		upath = "/" + upath
		r.URL.Path = upath
	}

	if r.Method == "POST" {
		uploadFile(w, r, f.root, path.Clean(upath))
	} else {
		serveFile(w, r, f.root, path.Clean(upath), true)
	}
}

